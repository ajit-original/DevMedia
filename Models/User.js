const mongoose = require('mongoose'); // importing mongoose

const UserSchema = new mongoose.Schema({ // defining user model
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = User = mongoose.model('user', UserSchema); // exporting User Schema module